template< class OlinkServiceAdapter, class InterfaceService>
void addService(std::vector < std::shared_ptr<InspectedService>>& services, ApiGear::ObjectLink::RemoteRegistry& registry)
{
    auto service = std::make_shared<InterfaceService>();
    auto olinkServiceAdapter = std::make_shared<OlinkServiceAdapter>(service, registry);
    auto inspectedService = std::make_shared<InspectedService>(olinkServiceAdapter);
    registry.addSource(inspectedService);
    services.push_back(inspectedService);
}


